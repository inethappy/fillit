#include "libft.h"

void	*ft_bzero(void *b, size_t n)
{
	size_t			i;
	unsigned char	*s;

	s = b;
	i = 0;
	while (i < n)
	{
		s[i] = '\0';
		i++;
	}
	return (s);
}
