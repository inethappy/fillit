#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = ft_strlen((char*)s);
	while ((*s != c) && *s)
	{
		s++;
		i--;
	}
	if ((i == 0) && (c != '\0'))
		return (NULL);
	return ((char*)s);
}
