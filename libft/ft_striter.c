#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	char	*tmp;
	int		i;

	if (s && s[0] && f)
	{
		i = 0;
		tmp = s;
		while (tmp[i])
		{
			f(&tmp[i]);
			i++;
		}
	}
}
