#include "libft.h"

int	ft_count_char(char *s, char c)
{
	int i;

	if ((s == NULL) || (c == '\0'))
		return (0);
	i = 0;
	while ((*s) && (i > -1))
	{
		if ((*s) == c)
			i++;
		s++;
	}
	if (i < -1)
		return (0);
	return (i);
}
